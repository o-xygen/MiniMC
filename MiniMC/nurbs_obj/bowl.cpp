GLfloat
bowl_diffuse[] = { 0.3, 0.3, 0.3, 1.0 },
bowl_specular[] = { .2, .2, .2, 1.0 },
bowl_shininess[] = { 20.0 };

void draw_bowl (void) {
  glColor4f(0.7, 0.7, 0.7, .4);
  glEnable (GL_BLEND);
  glBlendFunc (GL_ONE, GL_ONE);

  glClearColor (0.0, 0.0, 0.0, 0.0);
  glMaterialfv(GL_FRONT, GL_DIFFUSE, bowl_diffuse);
  glMaterialfv(GL_FRONT, GL_SPECULAR, bowl_specular);
  glMaterialfv(GL_FRONT, GL_SHININESS, bowl_shininess);

  glPushMatrix();
    glScalef(0.5, 0.5, 0.5);
   
    glPushMatrix();
      drawSurface2(borda_bowl, 4, 9, 3);
      glColor4f(0.7, 0.7, 0.7, .4);
    glPopMatrix();

	glPushMatrix();
		glRotatef(180, 0, 1, 0);
		glRotatef(180, 0, 0, 1);
		drawSurface2(borda_bowl_back, 4, 9, 3);
		glColor4f(0.7, 0.7, 0.7, .4);
	glPopMatrix();

    glPushMatrix();
      glScalef(0.9, 0.9, 1);
      glTranslatef(-0.30, 0, 0);
      drawSurface(fundo_bowl);
      glColor4f(0.7, 0.7, 0.7, .4);

      glPushMatrix();
        glRotatef(180, 0, 1, 0);
        glTranslatef(0, 0, 0.005);
        drawSurface(fundo_bowl_back);
        glColor4f(0.7, 0.7, 0.7, .4);
      glPopMatrix();

    glPopMatrix();

  glPopMatrix();
  glDisable (GL_BLEND);
}
